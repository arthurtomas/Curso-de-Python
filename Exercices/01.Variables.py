# Variables

# Strings
first_name = 'Arthur'
last_name = 'Barzilay'
full_name = first_name + ' ' + last_name
print('Hello ' + full_name)
print(type(full_name))

# int
age = 32
age += 1
print(f'Your age is {age}')
print(type(age))

# float
height = 182.5
print(f'Your height is {height}cm')
print(type(height))

# boolean
human = True
print(f'Are you a human? {human}')
print(type(human))
